using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace loop
{
    internal class _50to20
    {
        static void Main(string[] args)
        {
            for (int i = 50; i >= 20; i--) 
            {
                Console.WriteLine($" {i}");
            }
        }
    }
}
